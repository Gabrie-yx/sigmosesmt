-- 3) DDS
CREATE TABLE public.dds (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  data DATE NOT NULL DEFAULT CURRENT_DATE,
  hora TIME,
  gestor_id UUID REFERENCES public.dds_gestores(id) ON DELETE SET NULL,
  setor TEXT,
  tema_id UUID REFERENCES public.dds_temas(id) ON DELETE SET NULL,
  tema_livre TEXT,
  duracao_min INTEGER NOT NULL DEFAULT 10,
  conteudo TEXT,
  participantes_esperados INTEGER NOT NULL DEFAULT 0,
  participantes_presentes INTEGER NOT NULL DEFAULT 0,
  aderencia NUMERIC(5,2) GENERATED ALWAYS AS (
    CASE WHEN participantes_esperados > 0
      THEN ROUND((participantes_presentes::NUMERIC / participantes_esperados::NUMERIC) * 100, 2)
      ELSE 0 END
  ) STORED,
  status TEXT NOT NULL DEFAULT 'REALIZADO' CHECK (status IN ('REALIZADO','CANCELADO','PENDENTE')),
  incident_id UUID NULL,
  observacoes TEXT,
  created_by UUID,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);
CREATE INDEX idx_dds_data ON public.dds(data DESC);
CREATE INDEX idx_dds_setor ON public.dds(setor);
CREATE INDEX idx_dds_gestor ON public.dds(gestor_id);
ALTER TABLE public.dds ENABLE ROW LEVEL SECURITY;
CREATE POLICY dds_select ON public.dds FOR SELECT TO authenticated USING (true);
CREATE POLICY dds_insert ON public.dds FOR INSERT TO authenticated WITH CHECK (is_editor(auth.uid()));
CREATE POLICY dds_update ON public.dds FOR UPDATE TO authenticated USING (is_editor(auth.uid()));
CREATE POLICY dds_delete ON public.dds FOR DELETE TO authenticated USING (has_role(auth.uid(),'admin'::app_role));
CREATE TRIGGER trg_dds_updated BEFORE UPDATE ON public.dds FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

-- 4) ATTENDEES
CREATE TABLE public.dds_attendees (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  dds_id UUID NOT NULL REFERENCES public.dds(id) ON DELETE CASCADE,
  employee_id UUID NOT NULL,
  status TEXT NOT NULL DEFAULT 'PRESENTE' CHECK (status IN ('PRESENTE','AUSENTE','JUSTIFICADO')),
  observacao TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE(dds_id, employee_id)
);
CREATE INDEX idx_dds_att_dds ON public.dds_attendees(dds_id);
CREATE INDEX idx_dds_att_emp ON public.dds_attendees(employee_id);
ALTER TABLE public.dds_attendees ENABLE ROW LEVEL SECURITY;
CREATE POLICY dds_att_select ON public.dds_attendees FOR SELECT TO authenticated USING (true);
CREATE POLICY dds_att_insert ON public.dds_attendees FOR INSERT TO authenticated WITH CHECK (is_editor(auth.uid()));
