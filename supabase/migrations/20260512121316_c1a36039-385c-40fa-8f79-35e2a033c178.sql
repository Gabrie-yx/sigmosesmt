-- 1) TEMAS
CREATE TABLE public.dds_temas (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  codigo INTEGER UNIQUE,
  titulo TEXT NOT NULL,
  categoria TEXT NOT NULL DEFAULT 'GERAL' CHECK (categoria IN ('SIGNIFICATIVO','GERAL')),
  criticidade TEXT NOT NULL DEFAULT 'MEDIA' CHECK (criticidade IN ('ALTA','MEDIA','BAIXA')),
  ativo BOOLEAN NOT NULL DEFAULT true,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);
ALTER TABLE public.dds_temas ENABLE ROW LEVEL SECURITY;
CREATE POLICY dds_temas_select ON public.dds_temas FOR SELECT TO authenticated USING (true);
CREATE POLICY dds_temas_insert ON public.dds_temas FOR INSERT TO authenticated WITH CHECK (is_editor(auth.uid()));
CREATE POLICY dds_temas_update ON public.dds_temas FOR UPDATE TO authenticated USING (is_editor(auth.uid()));
CREATE POLICY dds_temas_delete ON public.dds_temas FOR DELETE TO authenticated USING (has_role(auth.uid(),'admin'::app_role));
CREATE TRIGGER trg_dds_temas_updated BEFORE UPDATE ON public.dds_temas FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

